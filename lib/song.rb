class Song
  def song_count=(count)
    @song_count = count
  end
  def song_count
    @song_count
  end

  def song_artists=(artists)
    @song_artists = artists
  end
  def song_artists
    @song_artists
  end

  def song_genres=(genres)
    @song_genres = genres
  end
  def song_genres
    @song_genres
  end



end
